-- 商品
CREATE TABLE COMMODITY(
    ID VARCHAR(13),
    -- 条形码 
    Names VARCHAR(10) NOT NULL,
    -- 商品名
    Brand VARCHAR(10),
    -- 品牌
    Cost BIGINT NOT NULL,
    -- 成本
    Price BIGINT NOT NULL,
    -- 价格
    Weights BIGINT NOT NULL,
    -- 重量（克）
    Types VARCHAR(12),
    -- 商品类型

    CHECK(ID LIKE '_____________'),
    -- 检查条形码是否为13位
    CHECK(Cost >= 0),
    -- 检查成本是否合法
    CHECK(Price >= 0),
    -- 检查价格是否合法
    CHECK(Weights >= 0),
    -- 检查重量是否合法
    CONSTRAINT PK_ID PRIMARY KEY (ID)
);


-- 库存
CREATE TABLE STOCK(
    COMMODITYID VARCHAR(13),
    -- 商品ID
    SHOPID BIGINT,
    -- 分店ID
    Amount BIGINT NOT NULL DEFAULT 0,
    -- 数量

    CHECK(Amount >= 0),
    -- 检查数量是否合法
    CONSTRAINT FK_STOCK_TO_COMMODITY FOREIGN KEY (COMMODITYID) REFERENCES COMMODITY(ID),
    CONSTRAINT FK_STOCK_TO_SHOP FOREIGN KEY (SHOPID) REFERENCES SHOP(ID)
);



-- 线上订单
CREATE TABLE ORDER_ONLINE(
    ID BIGINT,
    -- 订单ID
    USERID BIGINT,
    -- 用户ID
    SHOPID BIGINT,
    -- 分店ID
    COMMODITYID VARCHAR(13),
    -- 商品ID
    AmountOfCommodity BIGINT NOT NULL,
    -- 商品数量
    AmountOfMoney BIGINT NOT NULL,
    -- 金额
    OrderTime DATETIME NOT NULL,
    -- 下单时间
    DeliveryTime DATETIME NOT NULL,
    -- 送货时间
    DeliveryAddress VARCHAR(20) NOT NULL,
    -- 送货地址
    Phone VARCHAR(11) NOT NULL,
    -- 联系电话
    DeliveryPersonnelID BIGINT DEFAULT -1,
    -- 送货员工ID
    Status_ VARCHAR(3) DEFAULT '未完成',


    CHECK(Phone LIKE '___________'),
    -- 检查手机号是否合法（11位）
    CHECK(Status_ IN('完成', '未完成')),
    CONSTRAINT FK_USERS_TO_ORDER_ONLINE FOREIGN KEY (USERID) REFERENCES USERS(ID),
    CONSTRAINT FK_SHOP_TO_ORDER_ONLINE FOREIGN KEY (SHOPID) REFERENCES SHOP(ID),
    CONSTRAINT FK_COMMODITY_TO_ORDER_ONLINE FOREIGN KEY (COMMODITYID) REFERENCES COMMODITY(ID),
    CONSTRAINT FK_PERSONNEL_TO_ORDER_ONLINE FOREIGN KEY (DeliveryPersonnelID) REFERENCES PERSONNEL(ID)
);




-- 线下订单
CREATE TABLE ORDER_OFFLINE(
    ID BIGINT,
    -- 订单ID
    USERSID BIGINT,
    -- 用户ID
    SHOPID BIGINT,
    -- 分店ID
    COMMODITYID VARCHAR(13),
    -- 商品ID
    AmountOfCommodity BIGINT NOT NULL,
    -- 商品数量
    AmountOfMoney BIGINT NOT NULL,
    -- 金额
    OrderTime DATETIME NOT NULL,
    -- 结账时间
    OperatorID BIGINT,
    -- 操作员工ID

    CONSTRAINT FK_USERS_TO_ORDER_OFFLINE FOREIGN KEY (USERSID) REFERENCES USERS(ID),
    CONSTRAINT FK_SHOP_TO_ORDER_OFFLINE FOREIGN KEY (SHOPID) REFERENCES SHOP(ID),
    CONSTRAINT FK_COMMODITY_TO_ORDER_OFFLINE FOREIGN KEY (COMMODITYID) REFERENCES COMMODITY(ID),
    CONSTRAINT FK_PERSONNEL_TO_ORDER_OFFLINE FOREIGN KEY (OperatorID) REFERENCES PERSONNEL(ID)
);




