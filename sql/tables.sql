    CHECK(Sex IN('男', '女')),
    -- 检查性别是否合法
    CHECK(Age BETWEEN 6 AND 150),
    -- 检查年龄是否合法
    CHECK(Phone LIKE '___________'),
    -- 检查手机号是否合法（11位）
    CHECK(Balance >= 0),
    -- 检查余额是否合法
    CHECK(Points >= 0),
    -- 检查余额是否合法
    CONSTRAINT PK_USERS PRIMARY KEY (ID)
