-- 管理员
CREATE TABLE ADMINS (
    Account VARCHAR(10),
    Passwords VARCHAR(15) NOT NULL,
    CHECK(Passwords LIKE '_____%' ),
    -- 密码在5-15位之间
    CONSTRAINT PK_ADMINS PRIMARY KEY (Account)
);





-- 经理
CREATE TABLE MANAGER (
    ID BIGINT NOT NULL,
    Account VARCHAR(15) NOT NULL UNIQUE,
    Passwords VARCHAR(15) NOT NULL,
    RealName VARCHAR(5) NOT NULL,
    Sex VARCHAR(2) NOT NULL DEFAULT '男',
    Age INT DEFAULT 18,
    Phone VARCHAR(11) NOT NULL,
    Salary BIGINT DEFAULT 15000,
    Balance BIGINT DEFAULT 0,

    CHECK(Account LIKE '_____%' ),
    -- 账号在5-15位之间
    CHECK(Passwords LIKE '_____%' ),
    -- 密码在5-15位之间
    CHECK(Sex IN('男', '女')),
    -- 检查性别是否合法
    CHECK(Age BETWEEN 18 AND 60),
    -- 年龄在18-60岁
    CHECK(Phone LIKE '___________'),
    -- 检查手机号是否合法（11位）
    CHECK(Salary BETWEEN 15000 AND 50000),
    -- 检查工资是否合法
    CHECK(Balance >= 0),
    -- 检查余额是否合法
    CONSTRAINT PK_MANAGER PRIMARY KEY (ID)
);



-- 分店
CREATE TABLE SHOP(
    ID BIGINT,
    -- 分店ID
    Names VARCHAR(20),
    -- 分店名称 
    Addres VARCHAR(30) NOT NULL,
    -- 分店地址
    Phone VARCHAR(11) NOT NULL,
    -- 分店电话
    MANAGERID BIGINT NOT NULL UNIQUE,
    -- 经理ID
    CHECK(Phone LIKE '___________'),
